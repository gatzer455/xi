export function icon(name: string, options: { size?: number; color?: string; strokeWidth?: number } = {}): SVGElement {
  const { size = 16, color = 'currentColor', strokeWidth = 1.5 } = options;
  const svg = document.createElementNS(SVG_NS, 'svg');
  svg.setAttribute('width', String(size));
  svg.setAttribute('height', String(size));
  svg.setAttribute('viewBox', '0 0 24 24');
  svg.setAttribute('fill', 'none');
  svg.setAttribute('stroke', color);
  svg.setAttribute('stroke-width', String(strokeWidth));
  svg.setAttribute('stroke-linecap', 'round');
  svg.setAttribute('stroke-linejoin', 'round');

  const inner = ICONS[name];
  if (!inner) {
    console.warn(`Icon "${name}" not found`);
    return svg;
  }
  svg.innerHTML = inner;
  return svg;
}

export function getFileIconName(isDir: boolean, name: string): string {
  if (isDir) return 'folder';
  const ext = name.split('.').pop()?.toLowerCase() ?? '';
  const m: Record<string, string> = {
    md: 'file-text', txt: 'file-text', json: 'file-json',
    ts: 'file-code', js: 'file-code', jsx: 'file-code', tsx: 'file-code',
    rs: 'file-code', py: 'file-code', go: 'file-code', html: 'file-code', css: 'file-code',
  };
  return m[ext] ?? 'file';
}
